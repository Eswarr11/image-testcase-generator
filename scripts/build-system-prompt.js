const path = require('path');

const THRIVE_REPO_PATH = process.env.THRIVE_REPO_PATH
  || path.join(process.cwd(), '..', 'thrive_uiautomation_playwright');

const OUTPUT_PATH = path.join(process.cwd(), 'client', 'src', 'constants', 'systemPrompt.ts');

